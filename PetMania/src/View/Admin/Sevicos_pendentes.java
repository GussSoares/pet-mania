    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTServicos.getModel();
        modelo.setNumRows(0);
        ServicoDAO pdao = new ServicoDAO();

        for (Servicos s : pdao.read()) {

            modelo.addRow(new Object[]{
                s.getId(),
                s.getNome(),
                s.getEspecie(),
                s.getAltura(),
                s.getSexo(),
                s.getServico(),
                s.getObs(),
                s.getValorserv()
            });

        }

    }
    public void readJTableForDesc(String desc) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTServicos.getModel();
        modelo.setNumRows(0);
        ServicoDAO pdao = new ServicoDAO();

        for (Servicos s : pdao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
                s.getId(),
                s.getNome(),
                s.getEspecie(),
                s.getAltura(),
                s.getSexo(),
                s.getServico(),
                s.getObs(),
                s.getValorserv()
            });

        }

    }
    
