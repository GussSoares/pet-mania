    private void btRealizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRealizarActionPerformed
         if (jTServicos.getSelectedRow() != -1) {

            Servicos p = new Servicos();
            ServicoDAO dao = new ServicoDAO();

            p.setId((int) jTServicos.getValueAt(jTServicos.getSelectedRow(), 0));
            
            dao.delete(p);

            txtObser.setText("");
            txtServ.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }         
        
    }//GEN-LAST:event_btRealizarActionPerformed

