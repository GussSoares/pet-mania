    private void btComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btComprarActionPerformed
        
        Produtos p = new Produtos();/*Chamar banco de dados*/
        ProdutoDAO dao = new ProdutoDAO();;/*Chamar banco de dados*/
        
        int qtd = Integer.parseInt(txtQtd.getText());
        int qtdTabela = Integer.parseInt(jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 3).toString()); 
        int novoqtd =(qtdTabela-qtd);
        
        if(qtd>qtdTabela){
           JOptionPane.showMessageDialog(null,"Quntidade desejada acima do estoque.");
           txtQtd.setText("");
        }
        else if((txtProduto.equals(""))||(txtQtd.equals(""))){
           JOptionPane.showMessageDialog(null,"Preencha todos os campos.");
        
        }
        else if((txtValor.getText().isEmpty())){
           JOptionPane.showMessageDialog(null,"Realize o cálculo.");
        
        }
        else if(txtProduto.getText().isEmpty() || txtQtd.getText().isEmpty() || txtValor.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos", "ERRO", JOptionPane.ERROR_MESSAGE);  
        }

        else{
            JOptionPane.showMessageDialog(null,"Compra realizada com sucesso ;D ");
            
            p.setId((int) jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 0));
            p.setProduto(txtProduto.getText());
            p.setPreco((double) jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 2));       
            p.setQtd(novoqtd);
            dao.update(p);;/*Atualiza a tabela*/

            txtProduto.setText("");
            txtQtd.setText("");
            txtValor.setText("");
            
            readJTable();
            
            
        }
    }//GEN-LAST:event_btComprarActionPerformed

