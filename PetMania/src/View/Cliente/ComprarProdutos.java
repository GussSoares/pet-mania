    public void readJTable() {/*Inserir itens na tabela*/
        
        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for (Produtos p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),/*Itens da tabela*/
                p.getProduto(),
                p.getPreco(),
                p.getQtd()
            });

        }

    }
    public void readJTableForDesc(String desc) {/*Inserir itens na tabela*/
        
        DefaultTableModel modelo = (DefaultTableModel) jTProdutos.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for (Produtos p : pdao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
                p.getId(),/*Itens da tabela*/
                p.getProduto(),
                p.getPreco(),
                p.getQtd()
            });

        }

    }
    
